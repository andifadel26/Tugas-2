//ekspresi kondisional dengan boolean
public class KasusBoolean {
    public static void main(String[] args) {
        //kamus
        boolean bool;
        //algoritma
        bool = true;
        if (bool) {
            System.out.print("true \n");
        }else  
            System.out.print("false \n");
        if (!bool) {
            System.out.print("salah \n");
        }else 
            System.out.print("benar \n");
    }   
}